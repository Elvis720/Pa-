#include "cpu/cpu.h"
void add_SF(uint32_t res){
	cpu.eflags.SF = sign(res);
}
void add_ZF(uint32_t res,size_t data_size){
	res = res & (0xFFFFFFFF >> (32 -data_size));
	cpu.eflags.ZF =( res == 0);
}

void add_OF(uint32_t res,uint32_t dest,uint32_t src,size_t data_size){
	switch(data_size)
	{
		case 8:
			res = sign_ext(res,data_size);
			dest = sign_ext(dest,data_size);
			src = sign_ext(src,data_size);
			break;
		case 16:
			res = sign_ext(res,data_size);
			dest = sign_ext(dest,data_size);
			src = sign_ext(src,data_size);
			break;
		case 32:
			res = sign_ext(res,data_size);
			dest = sign_ext(dest,data_size);
			src = sign_ext(src,data_size);
			break;
	}
	if(sign(src) == sign(dest)){
		if(sign(res) != sign(dest)){
	 		cpu.eflags.OF = 1;
		}
		else{
			cpu.eflags.OF = 0;	
		}
	}
	else{
		cpu.eflags.OF = 0;
	}
}

void add_CF(uint32_t res,uint32_t src,size_t data_size){
	res = sign_ext(res,data_size);
	src = sign_ext(src,data_size);
	cpu.eflags.CF = res < src;
}
void add_PF(uint32_t res){
	int even = 0;
	switch(res & 0xf)
	{
	case 0x0:
	case 0x3:
	case 0x6:
	case 0xc:
	case 0xf:
 		even = 1;//偶数
		break;
	default:
		even = 0;//奇数
		break;
	}
	switch((res >> 4) &0xf)
	{
	case 0x0:
	case 0x3:
	case 0x6:
	case 0xc:
	case 0xf:
		if(even == 1){
			cpu.eflags.PF = 1;
		}
		else{
			cpu.eflags.PF = 0;
		}
	}
}
